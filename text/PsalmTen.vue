Why standest thou afar off, O LORD?<br>
Why hidest thou thyself in times of trouble?<br>
The wicked in his pride doth persecute the poor:<br>
Let them be taken in the devices that they have imagined.<br>
For the wicked boasteth of his heart's desire,<br>
And blesseth the covetous, whom the LORD abhorreth.<br>
The wicked, through the pride of his countenance, will not seek after God:<br>
God is not in all his thoughts.<br>
His ways are always grievous;<br>
Thy judgments are far above out of his sight:<br>
As for all his enemies, he puffeth at them.<br>
He hath said in his heart, I shall not be moved:<br>
For I shall never be in adversity.<br>
His mouth is full of cursing and deceit and fraud:<br>
Under his tongue is mischief and vanity.<br>
He sitteth in the lurking places of the villages:<br>
In the secret places doth he murder the innocent:<br>
His eyes are privily set against the poor.<br>
He lieth in wait secretly as a lion in his den:<br>
He lieth in wait to catch the poor:<br>
He doth catch the poor, when he draweth him into his net.<br>
He croucheth, and humbleth himself,<br>
That the poor may fall by his strong ones.<br>
He hath said in his heart, God hath forgotten:<br>
He hideth his face; he will never see it.<br>
Arise, O LORD; O God, lift up thine hand:<br>
Forget not the humble.<br>
Wherefore doth the wicked contemn God?<br>
He hath said in his heart, Thou wilt not require it.<br>
Thou hast seen it; for thou beholdest mischief and spite, to requite it with thy hand:<br>
The poor committeth himself unto thee;<br>
Thou art the helper of the fatherless.<br>
Break thou the arm of the wicked<br>
And the evil man: seek out his wickedness till thou find none.<br>
The LORD is King for ever and ever:<br>
The heathen are perished out of his land.<br>
LORD, thou hast heard the desire of the humble:<br>
Thou wilt prepare their heart, thou wilt cause thine ear to hear:<br>
To judge the fatherless and the oppressed,<br>
That the man of the earth may no more oppress.<br>
